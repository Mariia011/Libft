#include "libft.h"

void *ft_memset(void *b, int c, size_t len)
{
	char *str = (char *)b;
	size_t i; 
	if(len != 0 && b != 0)
	{
		i = 0;
		while(i <= len)
		{
			str[i] = c;
			i++;
		}
	}
	return str;
}

